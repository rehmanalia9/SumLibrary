package com.example.appsumlibrary;


public class Sum {

    public static int ADD(int a,int b)
    {

        return(a+b);
    }
    public static int SUB(int a,int b)
    {

        return(a-b);
    }
    public static int MUL(int a,int b)
    {

        return(a*b);
    }
    public static int Divide(int a,int b)
    {

        return(a/b);
    }


}
